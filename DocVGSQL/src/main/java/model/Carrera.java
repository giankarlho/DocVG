package model;

public class Carrera {
    
    
    private int CODCAR;
    private String ABRCAR;
    private String NOMCAR;
    private String YEARCAR;
    private String ESTCAR;

    public int getCODCAR() {
        return CODCAR;
    }

    public void setCODCAR(int CODCAR) {
        this.CODCAR = CODCAR;
    }

    public String getABRCAR() {
        return ABRCAR;
    }

    public void setABRCAR(String ABRCAR) {
        this.ABRCAR = ABRCAR;
    }

    public String getNOMCAR() {
        return NOMCAR;
    }

    public void setNOMCAR(String NOMCAR) {
        this.NOMCAR = NOMCAR;
    }

    public String getYEARCAR() {
        return YEARCAR;
    }

    public void setYEARCAR(String YEARCAR) {
        this.YEARCAR = YEARCAR;
    }

    public String getESTCAR() {
        return ESTCAR;
    }

    public void setESTCAR(String ESTCAR) {
        this.ESTCAR = ESTCAR;
    }

    
    
}
