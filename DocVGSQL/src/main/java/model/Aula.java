package model;

public class Aula {

    private int CODAUL;
    private String SECAUL;
    private String PROCAR;
    private String SEMAUL;
    private String CODCAR;

    public int getCODAUL() {
        return CODAUL;
    }

    public void setCODAUL(int CODAUL) {
        this.CODAUL = CODAUL;
    }

    public String getSECAUL() {
        return SECAUL;
    }

    public void setSECAUL(String SECAUL) {
        this.SECAUL = SECAUL;
    }

    public String getPROCAR() {
        return PROCAR;
    }

    public void setPROCAR(String PROCAR) {
        this.PROCAR = PROCAR;
    }

    public String getSEMAUL() {
        return SEMAUL;
    }

    public void setSEMAUL(String SEMAUL) {
        this.SEMAUL = SEMAUL;
    }

    public String getCODCAR() {
        return CODCAR;
    }

    public void setCODCAR(String CODCAR) {
        this.CODCAR = CODCAR;
    }

}
